function toggleImage(action) {
    const frontImage = document.querySelector('.front-image');
    const backImage = document.querySelector('.back-image');
    // تبديل شفافية الصور
    if (action === 'next') {
        frontImage.style.opacity = '0';
        backImage.style.opacity = '1';
    } else if (action === 'prev') {
        frontImage.style.opacity = '1';
        backImage.style.opacity = '0';
    } else {
        frontImage.style.opacity = frontImage.style.opacity === '0' ? '1' : '0';
        backImage.style.opacity = backImage.style.opacity === '0' ? '1' : '0';
    }
}


